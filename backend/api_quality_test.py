def test(method, path, expected, desc, headers=None, data=None, cat="General", timeout=30):
    """Test endpoint and record result"""
    url = f"{BASE_URL}{path}"
    start = time.time()
    try:
        if method == "GET":
            r = requests.get(url, headers=headers, timeout=timeout)
        elif method == "POST":
            r = requests.post(url, headers=headers, json=data, timeout=timeout)
        elif method == "PUT":
            r = requests.put(url, headers=headers, json=data, timeout=timeout)
        elif method == "DELETE":
            r = requests.delete(url, headers=headers, timeout=timeout)
        else:
            return None

        elapsed = round((time.time() - start) * 1000, 2)
        status = r.status_code
        passed = status == expected
        perf = "✅" if elapsed < 1000 else ("⚠️" if elapsed < 3000 else "❌")

        results.append({
            "method": method, "path": path.split('?')[0], "desc": desc,
            "expected": expected, "actual": status, "passed": passed,
            "time": elapsed, "perf": perf, "cat": cat,
            "sample": str(r.json())[:150] if r.text else "Empty"
        })

        icon = "✅" if passed else "❌"
        print(f"{icon} {method} {path} | Expected:{expected} Got:{status} | {elapsed}ms {perf} | {desc}")
        return r
    except Exception as e:
        elapsed = round((time.time() - start) * 1000, 2)
        results.append({
            "method": method, "path": path.split('?')[0], "desc": desc,
            "expected": expected, "actual": "ERROR", "passed": False,
            "time": elapsed, "perf": "❌", "cat": cat,
            "sample": str(e)
        })
        print(f"❌ {method} {path} | ERROR: {e}")
        return None

